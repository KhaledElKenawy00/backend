package com.virtualoffice.service.user.scheduler;

import com.virtualoffice.service.user.repository.VerificationRequestRepository;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

import java.time.LocalDateTime;

import static org.mockito.ArgumentMatchers.any;
import static org.mockito.Mockito.verify;

@ExtendWith(MockitoExtension.class)
class VerificationRequestSchedulerTest {

    @Mock
    private VerificationRequestRepository verificationRequestRepository;

    @InjectMocks
    private VerificationRequestScheduler scheduler;

    @Test
    void deleteExpiredRequestsDelegatesToRepository() {
        scheduler.deleteExpiredRequests();

        verify(verificationRequestRepository).deleteAllExpired(any(LocalDateTime.class));
    }
}
